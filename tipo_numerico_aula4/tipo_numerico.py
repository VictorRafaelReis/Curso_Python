"""
Tipo Numérico:

Existem 4 tipos numéricos em python:

    1 - int = inteiro:
        1.1 - a = 1
              type(a)
              <type 'int'>

              
    2 - float = ponto flutuante (num real)
        2.1 - a = 1.0
              type(a)
              <type 'float'>

              
    3 - boolean = lógico(v/f)
        3.1 - a = true
              type(a)
              <type 'bool'>

              
    4 - complex = complexo  #Para criar uma variável do tipo complexo basta adicionar um j após o valor
        4.1 - a = 4j

        4.2 - a = 4+3j
              type(a)
              <type 'complex'>


Pode-se utilizar o _ (underline) para separação de grandes valores
    ex:
        num = 1_000_000
        print(num)
        <1000000>
"""